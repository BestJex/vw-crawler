public class Company {
    private String name;
    private String title;


    @Override
    public String toString() {
        return "Company{" +
                "name='" + name + '\'' +
                ", title='" + title + '\'' +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
